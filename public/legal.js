function t(key) {
  const lang = localStorage.getItem(LANGUAGE_KEY) || "en";
  const active = I18N[lang] || I18N.en;
  const chunks = key.split(".");
  let cur = active;
  for (const c of chunks) cur = cur?.[c];
  if (cur !== undefined) return cur;
  cur = I18N.en;
  for (const c of chunks) cur = cur?.[c];
  return cur || key;
}

function setText(id, value) {
  const el = document.getElementById(id);
  if (el) el.textContent = value;
}

function applyLanguage(lang) {
  localStorage.setItem(LANGUAGE_KEY, lang);
  document.documentElement.lang = lang;
  document.title = t(`${page}.title`);

  setText("languageLabel", t("language"));
  setText("navBackTop", t("navBack"));
  setText("navPrivacyTop", t("navPrivacy"));
  setText("navTermsTop", t("navTerms"));
  setText("navCookiesTop", t("navCookies"));
  setText("navPrivacyBottom", t("navPrivacy"));
  setText("navTermsBottom", t("navTerms"));
  setText("navCookiesBottom", t("navCookies"));
  setText("supportPhonesLabel", t("supportPhones"));
  setText("updatedAt", t("updated"));
  setText("pageTitle", t(`${page}.h1`));
  setText("intro", t(`${page}.intro`));

  const keyList = [
    "s1t", "s1p", "s1i1", "s1i2", "s1i3", "s1i4",
    "s2t", "s2p", "s2i1", "s2i2", "s2i3", "s2i4",
    "s3t", "s3p", "s3i1", "s3i2", "s3i3",
    "s4t", "s4p", "s4i1", "s4i2", "s4i3",
    "s5t", "s5p",
    "s6t", "s6p",
    "s7t", "s7p",
    "s8t", "s8p",
    "s9t", "s9p",
  ];
  for (const k of keyList) {
    const value = t(`${page}.${k}`);
    const el = document.getElementById(k);
    if (el) el.textContent = value === `${page}.${k}` ? "" : value;
  }
}

